package com.cb.bl.fight;

public class Weapon {
    private int id;
    private String name;
    private int power;

    public Weapon( String name, int power) {

        this.name = name;
        this.power = power;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPower() {
        return power;
    }

    public void setPower(int power) {
        this.power = power;
    }
}
